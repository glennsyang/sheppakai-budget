<script lang="ts">
	import { Button } from '$lib/components/ui/button/index.js';
	import * as Dialog from '$lib/components/ui/dialog';
	import { Input } from '$lib/components/ui/input';
	import { Textarea } from '$lib/components/ui/textarea';
	import * as Select from '$lib/components/ui/select/index.js';
	import { enhance } from '$app/forms';
	import type { Category } from '$lib';
	import { toast } from 'svelte-sonner';

	interface Props {
		open: boolean;
		initialData?: {
			id?: string;
			amount?: number;
			description?: string;
			date?: string;
			categoryId?: string;
		};
		isEditing?: boolean;
		isLoading?: boolean;
		categories: Category[];
	}

	let {
		open = $bindable(),
		initialData,
		isEditing,
		isLoading = $bindable(false),
		categories
	}: Props = $props();

	let id = $state(initialData?.id || '');
	let amount = $state(initialData?.amount ? initialData.amount.toString() : '');
	let description = $state(initialData?.description || '');
	let date = $state(initialData?.date || '');
	let categoryId = $state(initialData?.categoryId || '');

	// Set default date to today
	function setDefaultDate() {
		if (!initialData?.date) {
			const today = new Date();
			date = today.toISOString().split('T')[0];
		}
	}

	$effect(() => {
		if (open) {
			if (initialData) {
				id = initialData.id || '';
				amount = initialData.amount ? initialData.amount.toString() : '';
				description = initialData.description || '';
				date = initialData.date || '';
				categoryId = initialData.categoryId || '';
			} else {
				id = '';
				amount = '';
				description = '';
				categoryId = '';
				setDefaultDate();
			}
		}
	});
</script>

<Dialog.Root bind:open>
	<Dialog.Content class="sm:max-w-[425px]">
		<Dialog.Header>
			<Dialog.Title>{isEditing ? 'Edit Expense' : 'Add New Expense'}</Dialog.Title>
			<Dialog.Description>
				{isEditing
					? 'Update this expense entry. Modify the amount, description, date, or category as needed.'
					: 'Record a new expense entry. Fill in the amount, description, date, and optionally select a category.'}
			</Dialog.Description>
		</Dialog.Header>
		<form
			class="space-y-4"
			method="POST"
			action={isEditing ? '/expense?/update' : '/expense?/create'}
			use:enhance={() => {
				open = false;
				isLoading = true;

				return async ({ result, update }) => {
					if (result.type === 'success') {
						toast.success(
							isEditing ? 'Expense updated successfully!' : 'Expense created successfully!'
						);
					} else {
						toast.error(`There was an error ${isEditing ? 'updating' : 'creating'} the expense.`);
					}
					isLoading = false;
					await update();
				};
			}}
		>
			<input type="hidden" name="id" value={id} />
			<div class="space-y-2">
				<label for="expense-amount" class="text-sm font-medium">Amount</label>
				<Input
					id="expense-amount"
					name="amount"
					type="number"
					step="0.01"
					min="0"
					bind:value={amount}
					placeholder="0.00"
					required
				/>
			</div>

			<div class="space-y-2">
				<label for="expense-description" class="text-sm font-medium">Description</label>
				<Textarea
					id="expense-description"
					name="description"
					bind:value={description}
					placeholder="What was this expense for?"
					rows={2}
				/>
			</div>

			<div class="space-y-2">
				<label for="expense-date" class="text-sm font-medium">Date</label>
				<Input id="expense-date" name="date" type="date" bind:value={date} required />
			</div>

			<div class="space-y-2">
				<label for="expense-category" class="text-sm font-medium">Category</label>
				<Select.Root type="single" name="categoryId" bind:value={categoryId} required>
					<Select.Trigger class="w-full">
						{categoryId
							? categories.find((c) => c.id === categoryId)?.name || 'Select a category'
							: 'Select a category'}
					</Select.Trigger>
					<Select.Content>
						<Select.Label class="px-2 py-1 text-sm font-medium">Categories</Select.Label>
						{#each categories as category (category.id)}
							<Select.Item value={category.id} label={category.name}>
								{category.name}
							</Select.Item>
						{/each}
					</Select.Content>
				</Select.Root>
			</div>

			<Dialog.Footer>
				<Dialog.Close><Button type="reset" variant="outline">Cancel</Button></Dialog.Close>
				<Button type="submit" disabled={!amount || !description || !date || !categoryId}>
					{isEditing ? 'Save' : 'Create'}
				</Button>
			</Dialog.Footer>
		</form>
	</Dialog.Content>
</Dialog.Root>
